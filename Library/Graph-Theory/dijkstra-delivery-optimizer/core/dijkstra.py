import heapq
from typing import Dict, List, Tuple, Optional, Union

# Define a Type Alias for clarity: A Graph is a Dictionary of Nodes 
# mapped to another Dictionary of {Neighbor: Weight}
Graph = Dict[str, Dict[str, Union[int, float]]]

def get_shortest_path(
    graph: Graph, 
    start_node: str
) -> Tuple[Dict[str, float], Dict[str, Optional[str]]]:
    """
    Calculates the shortest distance from a starting node to all other reachable 
    nodes in a weighted graph using Dijkstra's algorithm and a Min-Priority Queue.

    LOGIC OVERVIEW:
    The algorithm maintains a set of 'unvisited' nodes and calculates the 
    tentative distances. It always selects the node with the smallest 
    tentative distance to visit next (Greedy Approach).

    Args:
        graph: The adjacency list representation of the map.
        start_node: The identifier for the point of origin (e.g., 'Warehouse_01').

    Returns:
        A tuple containing:
        1. distances: A dictionary mapping each node to its minimum cost from start.
        2. predecessors: A dictionary to reconstruct the path (node -> previous_node).
    """

    # --- 1. Initialization ---
    # Every node starts at an 'Infinite' distance because we haven't found a path yet.
    distances = {node: float('inf') for node in graph}
    distances[start_node] = 0
    
    # Predecessors allow us to 'trace back' the optimal path once finished.
    predecessors = {node: None for node in graph}

    # The Priority Queue (PQ) stores tuples of (current_shortest_distance, node_name).
    # Python's heapq is a Min-Heap, so it always returns the smallest distance first.
    priority_queue = [(0, start_node)]

    # --- 2. The Main Loop ---
    while priority_queue:
        # Get the node with the current lowest distance from the Priority Queue
        current_distance, current_node = heapq.heappop(priority_queue)

        # Optimization: If we found a better path to this node already, skip processing
        if current_distance > distances[current_node]:
            continue

        # --- 3. Relaxation Step ---
        # Explore every neighbor of the current node
        for neighbor, weight in graph[current_node].items():
            path_distance = current_distance + weight

            # If this new path to the neighbor is shorter than any we've found before
            if path_distance < distances[neighbor]:
                distances[neighbor] = path_distance
                predecessors[neighbor] = current_node
                
                # Add this neighbor to the queue to explore its own neighbors later
                heapq.heappush(priority_queue, (path_distance, neighbor))

    return distances, predecessors


def reconstruct_path(predecessors: Dict[str, Optional[str]], target_node: str) -> List[str]:
    """
    Uses the predecessors dictionary to build the step-by-step route 
    to a specific destination.

    Args:
        predecessors: The parent-map generated by get_shortest_path.
        target_node: The destination node name.

    Returns:
        A list of node names representing the optimal path (e.g., ['A', 'B', 'C']).
    """
    path = []
    current = target_node
    
    while current is not None:
        path.append(current)
        current = predecessors.get(current)
    
    # The path is built backwards (target -> start), so we reverse it.
    return path[::-1]